package org.jhotdraw.samples.svg;

public class TextAreaRectangle {

    //Variables 
    private float rightMargin = 0;
    private float leftMargin = 0;
    private float verticalPos = 0;
    private float maxVerticalPos = 0;

    //Getter and setter for RightMargin
    public float getRightMargin() {
        return rightMargin;
    }
    public void setRightMargin(float rightMargin) {
        this.rightMargin = rightMargin;
    }

    //Getter and setter for LeftMargin
    public float getLeftMargin() {
        return leftMargin;
    }
    public void setLeftMargin(float leftMargin) {
        this.leftMargin = leftMargin;
    }

    //Getter and setter for VerticalPos
    public float getVerticalPos() {
        return verticalPos;
    }
    public void setVerticalPos(float verticalPos) {
        this.verticalPos = verticalPos;
    }

    //Getter and setter for MacVerticalPos
    public float getMaxVerticalPos() {
        return maxVerticalPos;
    }
    public void setMaxVerticalPos(float maxVerticalPos) {
        this.maxVerticalPos = maxVerticalPos;
    }


    public TextAreaRectangle() {}

}
